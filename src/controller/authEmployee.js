dotenv.config();

const createEmployeeController = asyncHandler(async (req, res) => {
  const { email, first_name, last_name, role, company_name, phone_number } =
    req.body;

  if (!email || !company_name || !phone_number || !first_name || !last_name) {
    res.status(400);
    throw new Error("Required fields are missing.");
  }

  const employee = await Employee.findOne({ email });

  if (employee) {
    res.status(400);
    throw new Error("Employee already exists.");
  }

  const password = crypto.randomBytes(20).toString("hex");
  const hashedPassword = await bcrypt.hash(password, 10);

  const employeeCreated = await Employee.create({
    email,
    password: hashedPassword,
    role,
    first_name,
    last_name,
    company_name,
    phone_number,
  });

  if (employeeCreated) {
    res.status(201).json({
      data: {
        id: employeeCreated.id,
        email: employeeCreated.email,
        password: password,
      },
      message:
        "Employee created successfully. Please save the auto generated password it cannot be retrieved.",
    });
  } else {
    res.status(404);
    throw new Error("Employee data is not valid.");
  }
});

const employeeLogin = asyncHandler(async (req, res) => {
  const { email, password } = req.body;

  if (!email || !password) {
    res.status(400);
    throw new Error("All fields are required.");
  }

  const employee = await Employee.findOne({ email });

  if (employee) {
    const verified = await bcrypt.compare(password, employee.password);
    if (verified) {
      const token = jwt.sign(
        {
          email: employee.email,
          first_name: employee.first_name,
          last_name: employee.last_name,
          id: employee._id,
          role: employee.role,
          company_name: employee.company_name,
          phone_number: employee.phone_number || "",
        },
        process.env.TOKEN_ACCESS,
        { expiresIn: "2h" }
      );

      res.status(200).json({
        data: {
          id: employee._id,
          email: employee.email,
          role: employee.role,
        },
        token,
        message: "Employee granted access.",
      });
    } else {
      res.status(403);
      throw new Error("Email or password is not valid.");
    }
  } else {
    res.status(404);
    throw new Error("Employee account not found.");
  }
});

